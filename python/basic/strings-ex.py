#!/usr/bin/env python3

test_string = "heLLo wOrLd"

print(test_string)
# heLLo wOrLd

# Note: In Python2:
# print test_string

print(test_string.title())
# Hello World

print(test_string.upper())
# HELLO WORLD

print(test_string.lower())
# hello world

exclamation_mark = "!"

print(test_string.title() + exclamation_mark)
# Hello World!

print("bad string   ".rstrip())
# bad string

print("  another bad string".lstrip())
# another bad string

print("  very bad string  ".strip())
# very bad string
