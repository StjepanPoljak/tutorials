# Python3 Number Examples

print(3 + 2)
# 5

print(3 - 2)
# 1

print(3 * 2)
# 6

print(3 / 2)
# 1.5

# Note: In Python2, result would be 1;
# To accomplish same behavior, use e.g. 3.0 / 2

print(3 ** 2)
# 9

print(3 % 2)
# 1

print("Meaning of life: " + str(42))
# Meaning of life: 42

if int("42") == 42:
    print("There is meaning!")
# There is meaning!
